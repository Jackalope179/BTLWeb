function dropList() {
  document.getElementById("dropdown-list").classList.toggle("show-display");
}
function dropListOut() {
  document.getElementById("dropdown-list").classList.remove("show-display");
}

function addClass(id) {
  document.getElementById(id).classList.toggle("show-display");
}

function addVisible(id) {
  document.getElementById(id).classList.toggle("show-visible");
}
